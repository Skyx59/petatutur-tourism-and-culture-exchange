// Fungsi klasifikasi destinasi menjadi: indoor, outdoor, nature, cultural
export function getDestType(name, category) {
    const nameLower = name.toLowerCase();
    const catLower = (category || '').toLowerCase();
    
    if (
        catLower.includes('museum') || 
        nameLower.includes('museum') || 
        catLower.includes('kerajinan') || 
        catLower.includes('seni') || 
        nameLower.includes('mansion') || 
        nameLower.includes('istana') || 
        nameLower.includes('house of') ||
        nameLower.includes('saung angklung')
    ) {
        return 'indoor';
    }
    
    if (
        catLower.includes('alam') || 
        catLower.includes('bahari') || 
        nameLower.includes('kawah') || 
        nameLower.includes('gunung') || 
        nameLower.includes('danau') || 
        nameLower.includes('pantai') || 
        nameLower.includes('telaga') || 
        nameLower.includes('air terjun') || 
        nameLower.includes('ngarai') || 
        nameLower.includes('gili') ||
        nameLower.includes('pulau') ||
        nameLower.includes('kebun')
    ) {
        return 'nature';
    }

    if (
        catLower.includes('religi') || 
        nameLower.includes('pura') || 
        nameLower.includes('masjid') || 
        nameLower.includes('gereja') || 
        nameLower.includes('katedral') || 
        nameLower.includes('makam') || 
        nameLower.includes('candi')
    ) {
        return 'outdoor';
    }

    return 'cultural';
}
